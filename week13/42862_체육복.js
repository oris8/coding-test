function solution(n, lost, reserve) {
  // ì—¬ë¶„ì˜ ì²´ìœ¡ë³µì„ ê°€ì ¸ì˜¨ í•™ìƒ ì¤‘ ë„ë‚œë‹¹í•œ í•™ìƒì„ ì œì™¸í•œ ë¦¬ìŠ¤íŠ¸
  const realReserve = reserve.filter((el) => !lost.includes(el));
  // ë„ë‚œë‹¹í•œ í•™ìƒ ì¤‘ ì—¬ë¶„ì˜ ì²´ìœ¡ë³µì„ ê°€ì ¸ì˜¨ í•™ìƒì„ ì œì™¸í•œ ë¦¬ìŠ¤íŠ¸
  const realLost = lost
    .filter((el) => !reserve.includes(el))
    .sort((a, b) => a - b);

  // ìˆ˜ì—…ì„ ë“¤ì„ ìˆ˜ ìˆëŠ” í•™ìƒ ìˆ˜ (ì „ì²´ í•™ìƒ ìˆ˜ - ë„ë‚œë‹¹í•œ í•™ìƒ ìˆ˜)
  let number = n - realLost.length;

  // ì—¬ë¶„ì˜ ì²´ìœ¡ë³µì„ ê°€ì§€ê³  ìˆëŠ” í•™ìƒë“¤ì˜ Set , hasë‘ delete ë©”ì„œë“œë¥¼ ì“°ê¸° ìœ„í•´ì„œ ì‚¬ìš©í–ˆìŠµë‹ˆë‹¤
  const reserveSet = new Set(realReserve);

  realLost.forEach((el) => {
    if (reserveSet.has(el - 1)) {
      number++;
      reserveSet.delete(el - 1);
    } else if (reserveSet.has(el + 1)) {
      number++;
      reserveSet.delete(el + 1);
    }
  });

  return number;
}
